// ─── DETECTION & HOOK ───────────────────────────
function detectLanguage() {
  const lang = (navigator.language || navigator.userLanguage || "en").toLowerCase();
  const base = lang.split("-")[0];
  if (translations[base]) return base;
  if (translations[lang]) return lang;
  return "en";
}

let currentLang = detectLanguage();
let currentStrings = { ...translations.en, ...(translations[currentLang] || {}) };

export function t(key) {
  return currentStrings[key] || translations.en[key] || key;
}

export function getLang() {
  return currentLang;
}

export function setLang(lang) {
  if (translations[lang]) {
    currentLang = lang;
    currentStrings = { ...translations.en, ...translations[lang] };
  }
}

export function getAvailableLanguages() {
  return Object.keys(translations).map(code => ({
    code,
    name: {
      en: "English", it: "Italiano", fr: "Français", de: "Deutsch",
      es: "Español", pt: "Português", ja: "日本語", ko: "한국어",
      zh: "中文", ar: "العربية",
    }[code] || code,
  }));
}
